# 문자열에 변수값을 넣는 3가지 방법
# (1) % 포맷팅
# %s:문자열타입, %d:정수타입, %f:실수타입
name, age, height = '홍길동', 20, 185.3
print("이름:%s, 나이:%d, 키:%f" %(name, age, height))
#이름:홍길동, 나이:20, 키:185.300000
print("이름:%d, 나이:%d, 키:%f" %(name, age, height)) #오류

# 자릿수 지정
print("이름:%10s, 나이:%4d, 키:%5.1f" %(name, age, height)) #자릿수를 맞춰준다. 이름자리에 10자리
# 이름:       홍길동, 나이:  20, 키:185.3 #그래서 이름자리에 10글자라서 홍길동 3자리 쓰고 7자리 남김
print("이름:%-10s, 나이:%04d, 키:%07.1f" %(name, age, height)) 
# 이름:홍길동       , 나이:0020, 키:00185.3 
#-를 사용해서 왼쪽정렬을 사용, 안넣으면 오른쪽 정렬

# (2) {} 포맷팅
print("이름:{}, 나이:{}, 키:{}".format(name, age, height)) 
# 이름:홍길동, 나이:20, 키:185.3 #자동으로 전체 값들이 들어가진다
print("이름:{0}{0}, 나이:{1}{0}, 키:{2}".format(name, age, height))
# 이름:홍길동홍길동, 나이:20홍길동, 키:185.3  #0이면 format 안에 들어있는 첫번째 변수값, 1은 두번째 변수값. 이렇게 변수의 값을 여러번 중복해서 문자열에 넣을 수 있다

#자릿수 지정
print("이름:{0:10}, 나이:{1:5}, 키:{2:5.2f}".format(name, age, height))
# 이름:홍길동       , 나이:   20, 키:185.30
# ^ : 가운데정렬, < : 왼쪽정렬, > : 오른쪽정렬(기본값)
print("이름:{0:^10}, 나이:{1:<5}, 키:{2:>5.2f}".format(name, age, height))
# 이름:   홍길동    , 나이:20   , 키:185.30

#자릿수 문자 채우기
print("이름:{0:-^10}, 나이:{1:0<5}, 키:{2:a>7.2f}".format(name, age, height))
# 이름:---홍길동----, 나이:20000, 키:a185.30  공백대신에 채울 문자열을 정할 수 있다

# (3) f=포맷팅 (파이썬 3.6버전부터 지원)
print(f"이름:{name}, 나이:{age}, 키:{height}") #f를 넣어줘야한다. 중괄호 안에는 변수명 입력
# 이름:홍길동, 나이:20, 키:185.3

# 자릿수 문자채우기
print(f"이름:{name:a^10}, 나이:{age:0>5}, 키:{height:+>10.2f}")
# 이름:aaa홍길동aaaa, 나이:00020, 키:++++185.30  남는 자리에 문자 채우는 기능
