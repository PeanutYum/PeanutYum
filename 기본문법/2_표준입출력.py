# (2) 반지름을 입력받고, 구의 부피 및 겉넓이를 출력하시오.
# 소숫점 첫째짜리까지만 표시하시오.
# 구의 부피 : 3/4*pi*r*r*r (pi=3.141592)
# 구의 겉넓이 : 4*pi*r*r (pi=3.141592)
# 출력결과 예시 : 구의부피 = 10.5, 구의 겉넓이 = 4.2
r = float(input("반지름 입력 > "))
pi = 3.141592
ballVol = 3/4*pi*r**3 #3/4*pi*r*r*도 가능하다 #줄바꿈을 하고싶다면 역슬래시(\)를 이용하기
ballArea = 4*pi*r**2
print(f'구의부피는{ballVol:.1f}이고 구의 겉넓이는{ballArea:.1f}입니다.')

